# 以下の和集合，差集合，交差，対象差をそれぞれ出力して下さい．
set_a = {'Levey', 'Cari', 'Luis', 'Kary', 'Everett', 'Goldia', 'Amory', 'Reine', 'Daveen', 'Boot'}
set_b = {'Cari', 'Belle', 'Levey', 'Everett', 'Lanny', 'Adrea', 'Boot', 'Celka', 'Helaina', 'Daveen'}

# 和集合
print(set_a | set_b)

# 差集合
print(set_a - set_b)
print(set_b - set_a)

# 交差
print(set_a & set_b)

# 対象差
print(set_a ^ set_b)
