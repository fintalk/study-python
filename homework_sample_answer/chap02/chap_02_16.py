# address に "東京都千代田区永田町1−7−1" を代入して下さい
address = "東京都千代田区永田町1−7−1"
# もし address に "千代田区"が入っていれば「千代田区です」とプリント，もし address に "新宿区"が入っていれば「新宿区です」とプリント，もし address に "渋谷区"が入っていれば，「渋谷区です」とプリントどれも入ってなければ「わかりません」とプリントして下さい．
if "千代田区" in address:
    print("千代田区です")
elif "新宿区" in address:
    print("新宿区です")
elif "渋谷区" in address:
    print("渋谷区です")
else:
    print("わかりません")

