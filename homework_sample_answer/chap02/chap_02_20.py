# リスト fruits_list を引数に取る関数、 print_fruits を定義し、引き取ったリストの要素を1つずつプリントする関数を定義して下さい。
def print_fruits(fruits_list):
    for fruit in fruits_list:
        print(fruit)

# 適当に作ったリストを print_fruits 関数に渡して実行して下さい。
print_fruits(["いちご", "みかん", "りんご"])
