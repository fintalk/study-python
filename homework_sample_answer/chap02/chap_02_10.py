# 1. xに"python" を代入
x = "python"
# 1. yに"python" を代入
y = "python"
# 1. もし x と y が同じであれば，「同じです！」とプリントする
if x == y:
    print("同じです！")
# 1. yに"javascript" を代入
y = "javascript"
# 1. もし x と y が同じであれば，「同じです！」とプリントする
if x == y:
    print("同じです！")

# 1. もし x と y が同じでなければ，「違います」とプリントする
if x != y:
    print("違います")
